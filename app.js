const state = {
  currentView: 'home',
  timelineFilter: 'All',
  data: null,
  error: null
};

const $ = (selector) => document.querySelector(selector);

async function init() {
  renderClock();
  setInterval(renderClock, 60000);
  renderLoading();
  try {
    const response = await fetch('./data/dashboard.json', { cache: 'no-store' });
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    state.data = await response.json();
    render();
  } catch (error) {
    state.error = error;
    renderError(error);
  }
}

function renderClock() {
  $('#clock').textContent = new Date().toLocaleString('fr-CH', {
    weekday: 'short', hour: '2-digit', minute: '2-digit', day: '2-digit', month: 'short'
  });
}

function renderLoading() {
  $('#nav').innerHTML = '<button class="active"><span>Loading</span><small>local data</small></button>';
  $('#kpi-grid').innerHTML = Array.from({ length: 6 }, () => `
    <article class="kpi-card skeleton-card">
      <p class="eyebrow">Loading</p>
      <div class="value">--</div>
      <div class="delta">Refreshing local sources</div>
    </article>
  `).join('');
  $('#view-content').innerHTML = `
    <section class="panel">
      <div class="panel-header">
        <div>
          <p class="eyebrow">Booting</p>
          <h3>Mission Control is loading local data</h3>
        </div>
      </div>
      <p class="muted">Reading JSON generated by refresh_data.py.</p>
    </section>
  `;
}

function renderError(error) {
  $('#nav').innerHTML = '<button class="active"><span>Setup needed</span><small>serve locally</small></button>';
  $('#view-title').textContent = 'Mission Control';
  $('#kpi-grid').innerHTML = '';
  $('#view-content').innerHTML = `
    <section class="panel">
      <div class="panel-header">
        <div>
          <p class="eyebrow">Data unavailable</p>
          <h3>Local JSON could not be loaded</h3>
        </div>
        <span class="tag blocked">Fix needed</span>
      </div>
      <p class="card-meta">${escapeHtml(error.message || String(error))}</p>
      <div class="stack">
        <article class="issue-card">
          <p class="card-title">1. Refresh the data</p>
          <p class="card-meta">python3 refresh_data.py</p>
        </article>
        <article class="issue-card">
          <p class="card-title">2. Serve the folder locally</p>
          <p class="card-meta">python3 -m http.server 8080</p>
        </article>
        <article class="issue-card">
          <p class="card-title">3. Open the app via HTTP</p>
          <p class="card-meta">http://localhost:8080</p>
        </article>
      </div>
    </section>
  `;
}

function renderNav() {
  const data = state.data;
  $('#nav').innerHTML = data.views.map(view => `
    <button class="${state.currentView === view.id ? 'active' : ''}" data-view="${view.id}">
      <span>${view.label}</span>
      <small>${view.hint}</small>
    </button>
  `).join('');

  $('#nav').querySelectorAll('button').forEach(btn => {
    btn.addEventListener('click', () => {
      state.currentView = btn.dataset.view;
      render();
    });
  });
}

function render() {
  const data = state.data;
  if (!data) return renderLoading();

  renderNav();
  renderSidebarMeta(data.meta);

  $('#kpi-grid').innerHTML = data.kpis.map(kpi => `
    <article class="kpi-card">
      <p class="eyebrow">${escapeHtml(kpi.label)}</p>
      <div class="value">${escapeHtml(kpi.value)}</div>
      <div class="delta">${escapeHtml(kpi.delta)}</div>
    </article>
  `).join('');

  const title = data.views.find(v => v.id === state.currentView)?.label || 'Mission Control';
  $('#view-title').textContent = title;

  const container = $('#view-content');
  container.innerHTML = '';
  const template = document.getElementById(`${state.currentView}-template`);
  container.appendChild(template.content.cloneNode(true));

  const renderers = {
    home: renderHome,
    agents: renderAgents,
    tasks: renderTasks,
    projects: renderProjects,
    decisions: renderDecisions,
    timeline: renderTimeline
  };
  renderers[state.currentView]();
}

function renderSidebarMeta(meta = {}) {
  const statusPill = document.querySelector('.status-pill.live');
  if (statusPill) statusPill.textContent = meta.statusLabel || 'Live local data';

  const sidebarTitle = document.querySelector('.sidebar-card h3');
  const sidebarNote = document.querySelector('.sidebar-card .muted');
  const sourceList = meta.sources || [];

  if (sidebarTitle) sidebarTitle.textContent = meta.focusOfDay || 'Unknown focus';
  if (sidebarNote) {
    sidebarNote.textContent = `${meta.focusNote || 'No focus note.'} Last refresh: ${meta.generatedAt || 'unknown'}`;
  }

  let sourceBlock = document.getElementById('source-list');
  if (!sourceBlock) {
    sourceBlock = document.createElement('div');
    sourceBlock.id = 'source-list';
    sourceBlock.className = 'stack source-list';
    document.querySelector('.sidebar-card').appendChild(sourceBlock);
  }
  sourceBlock.innerHTML = sourceList.slice(0, 4).map(source => `
    <div class="small muted">${escapeHtml(source)}</div>
  `).join('');
}

