function renderTimeline() {
  const data = state.data;
  const filters = ['All', ...new Set(data.timeline.map(item => item.type))];
  $('#filters').innerHTML = filters.map(filter => `
    <button class="filter-chip ${state.timelineFilter === filter ? 'active' : ''}" data-filter="${filter}">${filter}</button>
  `).join('');

  $('#filters').querySelectorAll('button').forEach(btn => {
    btn.addEventListener('click', () => {
      state.timelineFilter = btn.dataset.filter;
      renderTimeline();
    });
  });

  const items = state.timelineFilter === 'All'
    ? data.timeline
    : data.timeline.filter(item => item.type === state.timelineFilter);

  $('#full-timeline').innerHTML = renderCollection(items, renderTimelineItem, '<article class="issue-card">No timeline events for this filter.</article>');
}

function renderTimelineItem(item) {
  return `
    <article class="timeline-item">
      <div class="time">${escapeHtml(item.time)} · ${escapeHtml(item.actor)} · ${escapeHtml(item.project)}</div>
      <p class="card-title">${escapeHtml(item.type)}</p>
      <div class="card-meta">${escapeHtml(item.text)}</div>
    </article>
  `;
}

function renderCollection(items, mapper, fallback = '<article class="issue-card">No data available.</article>') {
  if (!items || !items.length) return fallback;
  return items.map(mapper).join('');
}

function normalizeTaskStatus(status) {
  const value = (status || '').toLowerCase();
  if (value === 'done') return 'Done';
  if (value === 'blocked') return 'Blocked';
  if (value === 'waiting') return 'Waiting';
  if (value === 'in progress') return 'In progress';
  if (value === 'ready') return 'Ready';
  if (value === 'inbox') return 'Inbox';
  return 'Inbox';
}

function statusClass(status = '') {
  const value = status.toLowerCase();
  if (value.includes('block')) return 'blocked';
  if (value.includes('wait')) return 'waiting';
  if (value.includes('run') || value.includes('progress') || value.includes('live')) return 'running';
  if (value.includes('done') || value.includes('track') || value.includes('ready')) return 'ontrack';
  if (value.includes('unknown')) return 'risk';
  return 'risk';
}

function healthClass(health = '') {
  return health.toLowerCase().replace(/\s+/g, '-');
}

function projectTag(health = '') {
  const h = health.toLowerCase();
  if (h.includes('blocked')) return 'blocked';
  if (h.includes('risk')) return 'waiting';
  if (h.includes('waiting')) return 'risk';
  if (h.includes('unknown')) return 'risk';
  return 'ontrack';
}

function escapeHtml(value) {
  return String(value ?? '').replace(/[&<>"']/g, char => ({
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#39;'
  })[char]);
}

